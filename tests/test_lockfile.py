class TestNpmLock:
    def test_parse_npm_v3(self, tmp_path, parser):
        lock_data = {
            "name": "my-app",
            "version": "1.0.0",
            "lockfileVersion": 3,
            "packages": {
                "": {"name": "my-app", "version": "1.0.0"},
                "node_modules/express": {
                    "version": "4.18.2",
                    "resolved": "https://registry.npmjs.org/express/-/express-4.18.2.tgz",
                    "integrity": "sha512-abc123",
                    "dependencies": {"body-parser": "1.20.2"},
                },
                "node_modules/body-parser": {
                    "version": "1.20.2",
                    "resolved": "https://registry.npmjs.org/body-parser/-/body-parser-1.20.2.tgz",
                },
            },
        }
        lock_file = tmp_path / "package-lock.json"
        lock_file.write_text(json.dumps(lock_data))

        info = parser.parse(lock_file)
        assert info is not None
        assert info.kind == LockfileKind.NPM
        assert info.lockfile_version == "3"
        assert info.package_count == 2  # excludes root ""

        express = info.get_package("express")
        assert express is not None
        assert express.version == "4.18.2"
        assert "body-parser" in express.dependencies

    def test_parse_npm_v1(self, tmp_path, parser):
        lock_data = {
            "name": "old-app",
            "lockfileVersion": 1,
            "dependencies": {
                "lodash": {
                    "version": "4.17.21",
                    "resolved": "https://registry.npmjs.org/lodash/-/lodash-4.17.21.tgz",
                    "integrity": "sha512-xyz",
                },
            },
        }
        lock_file = tmp_path / "package-lock.json"
        lock_file.write_text(json.dumps(lock_data))

        info = parser.parse(lock_file)
        assert info is not None
        assert info.lockfile_version == "1"
        lodash = info.get_package("lodash")
        assert lodash is not None
        assert lodash.version == "4.17.21"


