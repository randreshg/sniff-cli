class TestLockfileInfo:
    def test_package_count(self):
        info = LockfileInfo(
            kind=LockfileKind.CARGO,
            path=Path("Cargo.lock"),
            packages=(
                LockedDependency(name="foo", version="1.0.0"),
                LockedDependency(name="bar", version="2.0.0"),
            ),
        )
        assert info.package_count == 2

    def test_package_names(self):
        info = LockfileInfo(
            kind=LockfileKind.CARGO,
            path=Path("Cargo.lock"),
            packages=(
                LockedDependency(name="foo", version="1.0.0"),
                LockedDependency(name="bar", version="2.0.0"),
            ),
        )
        assert info.package_names == ("foo", "bar")

    def test_get_package_found(self):
        dep = LockedDependency(name="foo", version="1.0.0")
        info = LockfileInfo(
            kind=LockfileKind.CARGO,
            path=Path("Cargo.lock"),
            packages=(dep,),
        )
        assert info.get_package("foo") == dep

    def test_get_package_not_found(self):
        info = LockfileInfo(
            kind=LockfileKind.CARGO,
            path=Path("Cargo.lock"),
            packages=(),
        )
        assert info.get_package("nonexistent") is None

    def test_dependency_graph(self):
        info = LockfileInfo(
            kind=LockfileKind.CARGO,
            path=Path("Cargo.lock"),
            packages=(
                LockedDependency(name="app", version="1.0.0", dependencies=("lib",)),
                LockedDependency(name="lib", version="0.5.0", dependencies=("external",)),
            ),
        )
        graph = info.dependency_graph()
        assert graph["app"] == ("lib",)
        # "external" is not in packages, so filtered out
        assert graph["lib"] == ()

    def test_find_outdated(self):
        info = LockfileInfo(
            kind=LockfileKind.CARGO,
            path=Path("Cargo.lock"),
            packages=(
                LockedDependency(name="foo", version="1.0.0"),
                LockedDependency(name="bar", version="2.0.0"),
                LockedDependency(name="baz", version="3.0.0"),
            ),
        )
        latest = {"foo": "1.1.0", "bar": "2.0.0", "baz": "4.0.0"}
        outdated = info.find_outdated(latest)
        assert len(outdated) == 2
        assert ("foo", "1.0.0", "1.1.0") in outdated
        assert ("baz", "3.0.0", "4.0.0") in outdated


