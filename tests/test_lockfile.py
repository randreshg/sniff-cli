class TestDetectAndParse:
    def test_finds_multiple_lockfiles(self, tmp_path, parser):
        # Create Cargo.lock
        cargo_lock = tmp_path / "Cargo.lock"
        cargo_lock.write_text('version = 4\n\n[[package]]\nname = "foo"\nversion = "1.0.0"\n')

        # Create package-lock.json
        npm_lock = tmp_path / "package-lock.json"
        npm_lock.write_text(json.dumps({
            "lockfileVersion": 3,
            "packages": {
                "": {"name": "test"},
                "node_modules/bar": {"version": "2.0.0"},
            },
        }))

        results = parser.detect_and_parse(tmp_path)
        assert len(results) == 2
        kinds = {r.kind for r in results}
        assert LockfileKind.CARGO in kinds
        assert LockfileKind.NPM in kinds

    def test_empty_directory(self, tmp_path, parser):
        assert parser.detect_and_parse(tmp_path) == []

    def test_nonexistent_directory(self, tmp_path, parser):
        assert parser.detect_and_parse(tmp_path / "no_such_dir") == []

    def test_unknown_filename(self, tmp_path, parser):
        (tmp_path / "unknown.lock").write_text("data")
        assert parser.parse(tmp_path / "unknown.lock") is None
