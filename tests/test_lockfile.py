class TestCargoLock:
    def test_parse_cargo_lock(self, tmp_path, parser):
        lock_content = """\
# This file is automatically @generated by Cargo.
# It is not intended for manual editing.
version = 4

[[package]]
name = "serde"
version = "1.0.195"
source = "registry+https://github.com/rust-lang/crates.io-index"
checksum = "abc123"

[[package]]
name = "tokio"
version = "1.35.1"
source = "registry+https://github.com/rust-lang/crates.io-index"
dependencies = [
 "mio 0.8.10",
 "pin-project-lite 0.2.13",
]

[[package]]
name = "my-app"
version = "0.1.0"
dependencies = [
 "serde 1.0.195",
 "tokio 1.35.1",
]
"""
        lock_file = tmp_path / "Cargo.lock"
        lock_file.write_text(lock_content)

        info = parser.parse(lock_file)
        assert info is not None
        assert info.kind == LockfileKind.CARGO
        assert info.package_count == 3

        serde = info.get_package("serde")
        assert serde is not None
        assert serde.version == "1.0.195"
        assert serde.checksum == "abc123"

        tokio = info.get_package("tokio")
        assert tokio is not None
        assert "mio" in tokio.dependencies
        assert "pin-project-lite" in tokio.dependencies

    def test_parse_nonexistent(self, tmp_path, parser):
        assert parser.parse(tmp_path / "Cargo.lock") is None


